import UIKit

private let reuseIdentifier = "Cell"

class HYViewController: UICollectionViewController {
    
    private var colorArray: Array<UIColor> = []
    
    override func viewDidLoad() {
        super.viewDidLoad()

        // Uncomment the following line to preserve selection between presentations
        // self.clearsSelectionOnViewWillAppear = false

        // Register cell classes
        self.collectionView!.registerClass(UICollectionViewCell.self, forCellWithReuseIdentifier: reuseIdentifier)

        // Do any additional setup after loading the view.
        let numberOfColors = 100
        
        var tempArray: Array<UIColor> = []
        for _ in 0..<numberOfColors {
            let redValue = Float(arc4random() % 255) / 255.0
            let blueValue = Float(arc4random() % 255) / 255.0
            let greenValue = Float(arc4random() % 255) / 255.0
            
            tempArray.append(UIColor(colorLiteralRed: redValue, green: greenValue, blue: blueValue, alpha: 1.0))
        }
        
        colorArray = Array<UIColor>(tempArray)
    }

    override func didReceiveMemoryWarning() {
        super.didReceiveMemoryWarning()
        // Dispose of any resources that can be recreated.
    }

    /*
